      </el-table-column>

      <el-table-column align="center" label="栏目名">
        <template slot-scope="scope">
          <span>{{ scope.row.name }}</span>
        </template>
      </el-table-column>

      <el-table-column align="center" label="上级菜单">
        <template slot-scope="scope">
          <span>{{ scope.row.menu_name }}</span>
        </template>
      </el-table-column>

      <el-table-column align="left" label="路由">
